// ---------------------------------------------------------------------------
// Priority styling for digest items
// ---------------------------------------------------------------------------

function priorityDot(priority: 'high' | 'medium' | 'low') {
  switch (priority) {
    case 'high':
      return 'bg-red-400'
    case 'medium':
      return 'bg-amber-400'
    case 'low':
      return 'bg-sage-300'
  }
}

function priorityBg(priority: 'high' | 'medium' | 'low') {
  switch (priority) {
    case 'high':
      return 'bg-red-50 border-red-100'
    case 'medium':
      return 'bg-amber-50 border-amber-100'
    case 'low':
      return ''
  }
}

// ---------------------------------------------------------------------------
// Digest Section Card — renders one section of the new structured digest
// ---------------------------------------------------------------------------

function DigestSectionCard({ section }: { section: DigestSection }) {
  if (!section || !section.items) return null

  // Count high-priority items for badge
  const highCount = section.items.filter((i) => i.priority === 'high').length

  return (
    <div className="bg-surface border border-border rounded-xl p-6 shadow-sm">
      <div className="flex items-center justify-between mb-4">
        <h3 className="font-heading text-base font-semibold text-sage-900 flex items-center gap-2">
          <span className="text-lg">{section.icon}</span>
          {section.heading}
        </h3>
        {highCount > 0 && (
          <span className="text-xs font-medium px-2 py-0.5 rounded-full bg-red-100 text-red-700">
            {highCount} urgent
          </span>
        )}
      </div>

      {section.items.length > 0 ? (
        <ul className="space-y-2.5">
          {section.items.map((item, i) => (
            <li
              key={i}
              className={`flex items-start gap-2.5 text-sm rounded-lg p-2 -mx-2 border border-transparent ${priorityBg(item.priority)}`}
            >
              <span
                className={`flex-shrink-0 w-2 h-2 rounded-full mt-1.5 ${priorityDot(item.priority)}`}
              />
              <span className="text-sage-700 leading-relaxed">{item.text}</span>
            </li>
          ))}
        </ul>
      ) : (
        <p className="text-sm text-sage-400">Nothing to report in this section.</p>
      )}
    </div>
  )
}

