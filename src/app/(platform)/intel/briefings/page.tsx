// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

interface BriefingMetrics {
  new_inquiries: number
  tours_scheduled: number
  bookings: number
  lost_deals: number
  revenue_booked: number
}

// Digest section types (from the new weekly-digest generator)
interface DigestItem {
  text: string
  priority: 'high' | 'medium' | 'low'
  meta?: Record<string, unknown>
}

interface DigestSection {
  heading: string
  icon: string
  items: DigestItem[]
}

interface BriefingContent {
  summary: string
  metrics: BriefingMetrics
  demand_outlook: {
    score: number
    outlook: 'positive' | 'neutral' | 'caution'
  }
  trend_highlights: string[]
  weather_outlook: string
  anomaly_summary: string[]
  recommendations: string[]
  generated_at: string
  // New digest fields (present when generated by weekly-digest service)
  sections?: DigestSection[]
  title?: string
  week_start?: string
  week_end?: string
}

interface Briefing {
  id: string
  venue_id: string
  type: 'weekly' | 'monthly'
  content: BriefingContent
  created_at: string
  venues?: { name: string | null } | null
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function formatDate(dateStr: string): string {
  const d = new Date(dateStr)
  return d.toLocaleDateString('en-US', {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
    year: 'numeric',
  })
}

function formatDateTime(dateStr: string): string {
  const d = new Date(dateStr)
  return d.toLocaleDateString('en-US', {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
    year: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
  })
}

function formatCurrency(amount: number): string {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(amount)
}

function outlookStyles(outlook: 'positive' | 'neutral' | 'caution') {
  switch (outlook) {
    case 'positive':
      return 'bg-emerald-50 text-emerald-700 border border-emerald-200'
    case 'neutral':
      return 'bg-amber-50 text-amber-700 border border-amber-200'
    case 'caution':
      return 'bg-red-50 text-red-700 border border-red-200'
  }
}

function outlookLabel(outlook: 'positive' | 'neutral' | 'caution') {
  switch (outlook) {
    case 'positive':
      return 'Positive'
    case 'neutral':
      return 'Neutral'
    case 'caution':
      return 'Caution'
  }
}

