function buildCsvExport(snapshot: ChannelSnapshot, venueLabel: string): string {
  const lines: string[] = []
  lines.push(`venue,channel,window_days,computed_at`)
  lines.push(`"${venueLabel}","${snapshot.display_name}",${snapshot.window_days},${snapshot.computed_at_iso}`)
  lines.push('')
  lines.push('segment,unique_weddings,booked_weddings,tour_weddings,conversion_to_tour_rate,conversion_to_booked_rate,avg_booking_value_cents,v1_contaminated_pct')
  for (const c of snapshot.story_arc) {
    lines.push(
      `${c.segment},${c.unique_weddings},${c.booked_weddings},${c.tour_weddings},${c.conversion_to_tour_rate_0_1 ?? ''},${c.conversion_to_booked_rate_0_1 ?? ''},${c.avg_booking_value_cents ?? ''},${c.v1_contaminated_pct.toFixed(2)}`,
    )
  }
  lines.push('')
  lines.push('metric,value')
  lines.push(`spend_cents,${snapshot.cost_metrics.spend_cents}`)
  lines.push(`apparent_cac_cents,${snapshot.cost_metrics.cac_cents ?? ''}`)
  lines.push(`real_cac_excluding_broadcast_cents,${snapshot.cost_metrics.cac_excluding_broadcast_cents ?? ''}`)
  lines.push(`strict_cac_excluding_broadcast_and_crossplatform_cents,${snapshot.cost_metrics.cac_excluding_broadcast_and_crossplatform_cents ?? ''}`)
  lines.push(`inquiry_to_booked_rate,${snapshot.funnel.inquiry_to_booked_rate_0_1 ?? ''}`)
  lines.push(`avg_review_rating,${snapshot.quality_metrics.avg_review_rating ?? ''}`)
  lines.push(`review_count,${snapshot.quality_metrics.review_count}`)
  lines.push(`median_lead_time_days,${snapshot.quality_metrics.median_lead_time_days ?? ''}`)
  lines.push('')
  lines.push(`# ${watermarkFooter(snapshot, venueLabel)}`)
  return lines.join('\n')
}

