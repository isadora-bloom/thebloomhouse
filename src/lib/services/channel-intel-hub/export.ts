/**
 * Bloom House — Wave 25 Wedding MBA presentation export.
 *
 * Anchor docs:
 *   - feedback_deep_fix_vs_bandaid.md (every export embeds the full
 *     calibration band — sample sizes, prompt versions, freshness — so
 *     external readers can reproduce the number months later)
 *   - PROMPT-BIAS-AUDIT.md (v1 asterisks on every cell)
 *
 * Generates a static HTML snapshot (browser-printable to PDF) of the
 * channel story arc + CAC reveal + quality + calibration footer. The
 * caller renders this server-side, or the front-end window.print()'s
 * the iframe. CSV / JSON variants are also available.
 *
 * The export FREEZES the snapshot into channel_presentation_exports so
 * the share-token URL is reproducible.
 */

import type { SupabaseClient } from '@supabase/supabase-js'
import { createServiceClient } from '@/lib/supabase/service'
import type { ChannelSnapshot, NarrationResult } from './types'
import { randomBytes } from 'crypto'

export type ExportFormat = 'pdf' | 'pptx' | 'csv' | 'json'

export interface ExportArgs {
  venueId: string
  venueLabel: string
  snapshot: ChannelSnapshot
  narrator: NarrationResult | null
  format: ExportFormat
  exportedBy?: string | null
  supabase?: SupabaseClient
}

export interface ExportResult {
  share_token: string
  download_url: string
  content_type: string
  /** Raw body for download endpoints to stream. */
  body: string
}

function generateShareToken(): string {
  // 32 URL-safe chars
  return randomBytes(24).toString('base64url')
}

function fmt$(cents: number | null): string {
  if (cents === null) return 'n/a'
  return `$${(cents / 100).toFixed(0)}`
}

function fmtPct(r: number | null): string {
  if (r === null) return 'n/a'
  return `${(r * 100).toFixed(1)}%`
}

function watermarkFooter(snapshot: ChannelSnapshot, venueLabel: string): string {
  const v1Pct =
    snapshot.sample_sizes.ae_total > 0
      ? (snapshot.confidence_signals.v1_contaminated_count /
          snapshot.sample_sizes.ae_total) *
        100
      : 0
  const promptVersions =
    snapshot.confidence_signals.prompt_versions_used.length === 0
      ? '(forensic-rule-only, no LLM judge)'
      : snapshot.confidence_signals.prompt_versions_used.join(', ')
  return `Generated by Bloom · bloomhq.ai · ${venueLabel} · ${snapshot.computed_at_iso} · v1-contam ${v1Pct.toFixed(1)}% · prompts: ${promptVersions}`
}

