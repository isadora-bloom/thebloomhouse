/**
 * Build the export body in the requested format and persist a frozen
 * snapshot row into channel_presentation_exports. Returns the share
 * token + download URL.
 */
export async function generateExport(args: ExportArgs): Promise<ExportResult> {
  const sb = args.supabase ?? createServiceClient()
  const shareToken = generateShareToken()
  const exportedAt = new Date().toISOString()

  let body: string
  let contentType: string
  if (args.format === 'csv') {
    body = buildCsvExport(args.snapshot, args.venueLabel)
    contentType = 'text/csv; charset=utf-8'
  } else if (args.format === 'json') {
    body = JSON.stringify(
      {
        venue_label: args.venueLabel,
        snapshot: args.snapshot,
        narrator: args.narrator,
        exported_at: exportedAt,
        watermark: watermarkFooter(args.snapshot, args.venueLabel),
      },
      null,
      2,
    )
    contentType = 'application/json; charset=utf-8'
  } else {
    // 'pdf' and 'pptx' both deliver HTML — the front-end print-to-pdf's
    // the HTML or the operator runs the page through Print → PDF. A
    // future Wave will swap in a real PDF library; the share-token
    // contract stays the same.
    body = buildHtmlExport(args.snapshot, args.narrator, args.venueLabel)
    contentType = 'text/html; charset=utf-8'
  }

  // Persist the frozen snapshot.
  const snapshotJsonb = {
    channel_slug: args.snapshot.channel_slug,
    source_platform: args.snapshot.source_platform,
    venue_label: args.venueLabel,
    window_days: args.snapshot.window_days,
    computed_at_iso: args.snapshot.computed_at_iso,
    story_arc: args.snapshot.story_arc,
    cost_reveal: args.snapshot.cost_metrics,
    funnel: args.snapshot.funnel,
    quality: args.snapshot.quality_metrics,
    sample_sizes: args.snapshot.sample_sizes,
    confidence_signals: args.snapshot.confidence_signals,
    narrator_output: args.narrator,
  }

  const { error } = await sb.from('channel_presentation_exports').insert({
    venue_id: args.venueId,
    exported_at: exportedAt,
    exported_by: args.exportedBy ?? null,
    channel_slug: args.snapshot.channel_slug,
    format: args.format,
    share_token: shareToken,
    snapshot_jsonb: snapshotJsonb,
  })
  if (error) {
    // Soft-fail: still return the body. The operator gets the export,
    // the audit row is missing (lower priority).
    // eslint-disable-next-line no-console
    console.warn('[channel-intel-hub.export] persist failed:', error.message)
  }

  return {
    share_token: shareToken,
    download_url: `/api/public/channels/exports/${shareToken}`,
    content_type: contentType,
    body,
  }
}

/**
 * Render a frozen snapshot (stored in channel_presentation_exports) as
 * HTML so the public-share endpoint can serve it without re-deriving.
 */
export function renderFrozenExport(
  snapshotJsonb: {
    venue_label?: string
    channel_slug?: string
    source_platform?: string
    window_days?: number
    computed_at_iso?: string
    story_arc?: unknown
    cost_reveal?: unknown
    funnel?: unknown
    quality?: unknown
    sample_sizes?: unknown
    confidence_signals?: unknown
    narrator_output?: unknown
  },
): string {
  // Lightweight render: reconstruct the ChannelSnapshot shape enough for
  // the HTML renderer. The frozen jsonb is exhaustive enough that we
  // don't need to re-derive anything.
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const s = snapshotJsonb as any
  const fake: ChannelSnapshot = {
    venue_id: '',
    channel_slug: s.channel_slug ?? 'unknown',
    source_platform: s.source_platform ?? 'unknown',
    display_name: s.channel_slug ?? 'unknown',
    computed_at_iso: s.computed_at_iso ?? new Date().toISOString(),
    window_days: s.window_days ?? 90,
    role_breakdown: { acquisition: 0, validation: 0, conversion: 0, mixed: 0, unknown: 0 },
    intent_breakdown: { targeted: 0, broadcast: 0, validation: 0, unknown: 0 },
    funnel: s.funnel ?? {
      inquiries: 0,
      tours: 0,
      booked: 0,
      inquiry_to_tour_rate_0_1: null,
      tour_to_booked_rate_0_1: null,
      inquiry_to_booked_rate_0_1: null,
      drop_inquiry_to_tour_0_1: null,
      drop_tour_to_booked_0_1: null,
    },
    cost_metrics: s.cost_reveal ?? {
      spend_cents: 0,
      cac_cents: null,
      cac_excluding_broadcast_cents: null,
      cac_excluding_broadcast_and_crossplatform_cents: null,
      cost_per_inquiry_cents: null,
      cost_per_tour_cents: null,
    },
    quality_metrics: s.quality ?? {
      avg_booking_value_cents: null,
      median_lead_time_days: null,
      avg_review_rating: null,
      review_count: 0,
      persona_distribution: {},
    },
    sample_sizes: s.sample_sizes ?? {
      unique_weddings: 0,
      ae_total: 0,
      weddings_per_role: { acquisition: 0, validation: 0, conversion: 0, mixed: 0, unknown: 0 },
      weddings_per_intent: { targeted: 0, broadcast: 0, validation: 0, unknown: 0 },
      weddings_per_story_arc: {
        discovery: 0,
        inquiry: 0,
        validation: 0,
        broadcast: 0,
        cross_platform_footprint: 0,
      },
    },
    confidence_signals: s.confidence_signals ?? {
      v1_contaminated_count: 0,
      v2_classified_count: 0,
      null_classified_count: 0,
      data_freshness_iso: new Date().toISOString(),
      prompt_versions_used: [],
      window_days: s.window_days ?? 90,
      computed_with_function: 'computeChannelSnapshot',
    },
    story_arc: s.story_arc ?? [],
    disagreement_findings_count: 0,
  }
  return buildHtmlExport(fake, s.narrator_output ?? null, s.venue_label ?? 'Venue')
}
